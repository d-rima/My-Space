def get_valid_word(words):
    word = random.choice(words)
    while '-' in word or ' ' in word:
        word = random.choice(words)

    return word.upper()

def play():
    word = get_valid_word(words)
    word_letters = set(word)
    used_letters = set()
    alphabet = set(string.ascii_uppercase)
    lives = 7

    while len(word_letters) > 0 and lives > 0:
        word_list = [letter if letter in used_letters else '-' for letter in word]
        print('you have', lives, 'lives left')
        print('You have guessed: ', ' '.join(used_letters))
        print('current word: ', ' '.join(word_list))

        user_choice = input('Guess a letter: ').upper()
        used_letters.add(user_choice)
        if user_choice in alphabet and user_choice in word:
            word_letters.remove(user_choice)
        elif user_choice in alphabet and user_choice not in word_letters:
            print('Not in the word, guess again')
            lives = lives - 1
        else:
            print('Illegal character, guess again')
    
    if len(word_letters) == 0:
        print(f'Congrats, you gussed the word {word}')
    else:
        print(f'You ran out of guesses. The word was {word}')
        
play()
